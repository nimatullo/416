function compute_avgs(data) {
  let avgs = {};
  for (let key in data) {
    let sum = 0;
    for (let inner_key in data[key]) {
      sum += data[key][inner_key];
    }
    avgs["avg_" + key] = sum / Object.keys(data[key]).length;
  }
  return avgs;
}

